#!/usr/bin/env node
/**
 * Builds static-pages/src/index.html into per-environment, publishable copies under
 * static-pages/dist/<env>/index.html (F3Go30-5nfj.2 follow-up).
 *
 * Two environments, same split as the GAS deploy targets (tools/manage-deployments.js):
 *   sit  -> static-pages/dist/sit/index.html
 *   prod -> static-pages/dist/prod/index.html
 *
 * This build step stamps two placeholders in the source:
 *
 *   1. STATIC_BUILD_VERSION_ (`var STATIC_BUILD_VERSION_ = null;`) — a version string in the same
 *      shape script/version.js gets stamped with (manage-deployments.js's stampVersion):
 *      "<version>.<build>" for sit, bare "<version>" for prod. This is only ever a fast, offline
 *      first-paint value; the page always reconciles it with the live GAS-reported version on its
 *      first identify call (see static-pages/src/index.html's applyServerConfig_), so it does not
 *      need to match whichever version is actually live on that environment's deployment at build
 *      time.
 *   2. STATIC_WEBAPP_URL_ (`var STATIC_WEBAPP_URL_ = null;`) — the env's GAS webapp /exec URL,
 *      derived from the deployment ID in local.settings.json (testDeploymentId=sit,
 *      templateDeploymentId=prod — the same key mapping tools/callWebapp.js uses). This is the
 *      baked default backend the page calls when opened WITHOUT a ?webapp= query param, so a bare
 *      or bookmarked URL is no longer a dead page (F3Go30-6bl6). A ?webapp= param still overrides
 *      it when present. Left null in the unbuilt source so local/Playwright runs (which pass
 *      ?webapp= explicitly) work straight from static-pages/src.
 *
 * Also copies static-pages/src/how-it-works.html (F3Go30-e3co, generated by
 * tools/sync-how-it-works.js) into each env's dist/ folder unchanged — no stamping needed.
 *
 * Usage:
 *   node tools/build-static-pages.js [--env sit|prod|all]   (default: all)
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const SRC_PATH = path.join(ROOT, 'static-pages', 'src', 'index.html');
const HOW_IT_WORKS_SRC_PATH = path.join(ROOT, 'static-pages', 'src', 'how-it-works.html');
const ASSETS_SRC_DIR = path.join(ROOT, 'static-pages', 'src', 'assets');
const DIST_ROOT = path.join(ROOT, 'static-pages', 'dist');
const PKG_PATH = path.join(ROOT, 'package.json');
const SETTINGS_PATH = path.join(ROOT, 'local.settings.json');

const VERSION_PLACEHOLDER = 'var STATIC_BUILD_VERSION_ = null;';
const WEBAPP_PLACEHOLDER = 'var STATIC_WEBAPP_URL_ = null;';

// env -> local.settings.json key holding that env's deployment ID (mirrors callWebapp.js ENVS).
const DEPLOYMENT_ID_KEY = { sit: 'testDeploymentId', prod: 'templateDeploymentId' };

function versionStringFor(env, pkg) {
  if (env === 'sit') return `${pkg.version}.${pkg.build || 0}`;
  return String(pkg.version);
}

// Resolves the env's GAS webapp /exec base URL from its deployment ID. Throws (rather than
// stamping an empty/partial URL) when the ID is absent, so a misconfigured settings file fails
// the build loudly instead of shipping a dead default backend.
function execUrlForEnv_(env, settings) {
  const key = DEPLOYMENT_ID_KEY[env];
  if (!key) throw new Error(`build-static-pages: unknown env '${env}'`);
  const deploymentId = settings[key];
  if (!deploymentId) {
    throw new Error(`build-static-pages: ${key} is not set in local.settings.json (needed to bake the ${env} webapp URL)`);
  }
  return `https://script.google.com/macros/s/${deploymentId}/exec`;
}

// Pure string transform: swaps both source placeholders for their stamped values. Kept
// filesystem-free so it is unit-testable (test/test_build_static_pages.js); buildOne wires it
// to the real files. Throws if either placeholder is missing, guarding against a source edit
// that silently drops one.
function stampSource_(src, { versionString, webAppUrl }) {
  if (!src.includes(VERSION_PLACEHOLDER)) {
    throw new Error(`static-pages/src/index.html: expected placeholder not found: ${VERSION_PLACEHOLDER}`);
  }
  if (!src.includes(WEBAPP_PLACEHOLDER)) {
    throw new Error(`static-pages/src/index.html: expected placeholder not found: ${WEBAPP_PLACEHOLDER}`);
  }
  return src
    .replace(VERSION_PLACEHOLDER, `var STATIC_BUILD_VERSION_ = ${JSON.stringify(versionString)};`)
    .replace(WEBAPP_PLACEHOLDER, `var STATIC_WEBAPP_URL_ = ${JSON.stringify(webAppUrl)};`);
}

function buildOne(env) {
  const pkg = JSON.parse(fs.readFileSync(PKG_PATH, 'utf8'));
  const settings = JSON.parse(fs.readFileSync(SETTINGS_PATH, 'utf8'));
  const versionString = versionStringFor(env, pkg);
  const webAppUrl = execUrlForEnv_(env, settings);
  const src = fs.readFileSync(SRC_PATH, 'utf8');
  const out = stampSource_(src, { versionString, webAppUrl });
  const outDir = path.join(DIST_ROOT, env);
  fs.mkdirSync(outDir, { recursive: true });
  fs.writeFileSync(path.join(outDir, 'index.html'), out, 'utf8');
  // Small companion file the GAS About dialog fetches (UrlFetchApp) to show the live static
  // page's own build stamp alongside APP_VERSION — see script/onOpen.js's showAbout().
  fs.writeFileSync(path.join(outDir, 'version.json'), JSON.stringify({ version: versionString }), 'utf8');
  // Static assets (currently just the favicon) — copied as-is, no stamping.
  const assetsOutDir = path.join(outDir, 'assets');
  fs.mkdirSync(assetsOutDir, { recursive: true });
  for (const entry of fs.readdirSync(ASSETS_SRC_DIR)) {
    fs.copyFileSync(path.join(ASSETS_SRC_DIR, entry), path.join(assetsOutDir, entry));
  }
  // "How it Works" content page (F3Go30-e3co) — generated by tools/sync-how-it-works.js from
  // docs/Go30-Intro.md. Copied as-is: it has no server calls or version dependency, so unlike
  // index.html above it doesn't need per-env stamping — both envs serve identical content.
  if (fs.existsSync(HOW_IT_WORKS_SRC_PATH)) {
    fs.copyFileSync(HOW_IT_WORKS_SRC_PATH, path.join(outDir, 'how-it-works.html'));
  }
  console.log(`built static-pages/dist/${env}/index.html (v${versionString}, webapp ${webAppUrl})`);
}

function main() {
  const args = process.argv.slice(2);
  const envIdx = args.indexOf('--env');
  const env = envIdx !== -1 ? args[envIdx + 1] : 'all';
  const envs = env === 'all' ? ['sit', 'prod'] : [env];
  envs.forEach(buildOne);
}

if (require.main === module) {
  main();
}

module.exports = { stampSource_, execUrlForEnv_, versionStringFor, buildOne };
